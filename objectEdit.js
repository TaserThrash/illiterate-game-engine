function deepCopy(obj) {
    if (typeof obj !== "object" || obj === null) {
      return obj; // Handle primitives and null
    }

    if (obj instanceof p5.Image || obj instanceof p5.Graphics) {
        console.log("hello me is image me is problem");
        return obj;
    }
  
    let newObj = {};
    for (let key in obj) {
        console.log(key);
        if (key === "brain") {
            newObj[key] = deepCopyBrain(obj[key]); // Handle the brain separately
        }
        else if (key == "Image") {
            newObj[key] = obj[key];
        }
        else {
            newObj[key] = deepCopy(obj[key]); // Recursively copy other properties
        }
    }

    return newObj;
  }
  
  function deepCopyBrain(brain) {
    let newBrain = [[], [], []];
    for (let i = 0; i < 3; i++) {
      for (let j = 0; j < brain[i].length; j++) {
        let k = brain[i][j];
        let bag = deepCopyBrainComponent(brain[i][j]);
        if(i == 3){
            bag.act = k.act;
        }
        else{
            bag.evaluate = k.evaluate;
        }

        newBrain[i].push(bag); // Copy each brain component
        console.log(bag);
      }
    }

    return newBrain;
  }
  
  function deepCopyBrainComponent(component) {
    let newComponent = {};
    for (let key in component) {
      if (key === "image") {
        let im = createGraphics(400, 100);
        im.image(component.image, 0, 0, 400, 100);
        
        newComponent[key] = im; // Copy the image using p5.js's copy()
      }
      else {
        newComponent[key] = component[key]; // Copy other properties directly
      }
    }
    return newComponent;
  }
