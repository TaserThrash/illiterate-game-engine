function arr2img(grph, img, x, y, w, h){
    grph.push();
    grph.noStroke();
    grph.translate(x, y);
    for(let y = 0; y < img.length; y++){
        for(let x = 0; x < img[y].length; x++){
            grph.fill(["#000000", "#ff2343"][img[y][x]]);
            grph.rect(w * (x / img[y].length), h * (y / img.length), w / img[y].length, h / img.length);
        }
    }
    grph.pop();
}

