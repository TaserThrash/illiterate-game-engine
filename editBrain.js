function nthRoot(a, n) {
    if (a < 0 && n % 2 == 0) {
      return NaN;
    }
    return Math.sign(a) * Math.pow(Math.abs(a), 1/n);
}

function distance(x, y, xx, yy){
    return ((x - xx) ** 2 + (y - yy) ** 2) ** 0.5;
}

